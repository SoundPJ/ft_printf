#include "../ft_printf.h"

void	ft_putstr(t_cv *spcf, char *s)
{
	if (!s)
	{
		write(1, "(null)", 6);
		spcf->len += 6;
		return ;
	}
	while (*s != '\0')
	{
		write(1, s++, 1);
		spcf->len += 1;
	}
}
